let numberString = "25";                 // число, записанное в текстовом формате

// 2. Создать переменную типа число
let num = 15;

// 3. Преобразовать строку с числом в числовой тип данных и сложить с num
let convertedNumber = Number(numberString);   // преобразование строки в число
let result = convertedNumber + num;           // сложение

console.log("Результат сложения:", result);   // вывод результата

